def multiplicationAndSumOfTwoNumbers(x, y):
    multiplication = x * y
    sum = x + y

    print(f"Multiplication of {x} and {y}:", multiplication)
    print(f"Sum of {x} and {y} :", sum)

x = int(input("Enter the first number: "))
y = int(input("Enter the second number: "))
multiplicationAndSumOfTwoNumbers(x, y)


#EXAMPLE
# Enter the first number: 4
# Enter the second number: 5
# Multiplication of 4 and 5: 20
# Sum of 4 and 5 : 9